	public static List<String> getEmojiData(String str) {
		String[] strs = str.split(",");
		List<String> datas = new ArrayList<String>();
		
		for (String s : strs) {
			String[] ss = s.split("\\.\\.");
			if (ss.length < 2) {
				datas.addAll(Arrays.asList(s.split("\\.\\.")));
				continue;
			}
			
			int begin = Integer.parseInt(ss[0], 16);
			int end = Integer.parseInt(ss[1], 16);
			for (;begin <= end; begin++) {
				if (begin > 0xFFFF) {
					datas.add(Integer.toString(begin, 16).toUpperCase());
				} else {
					datas.add(String.format("%04x", begin).toUpperCase());
				}
			}
		}
		
		return datas;
	}
	
